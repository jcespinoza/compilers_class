random.seed(time.clock())

x = random.randint(0, 100)
y = random.randint(0, 100)

print ("x = ", x)
print ("y = ", y)

# Greather than
print ("x > y: ", x > y)

# Less than
print ("x < y: ", x < y)

# Greather or Equal
print("x >= y: ", x >= y)

# Less or Equal
print ("x <= y: ", x <= y)

# Not Equal
print ("x != y: ", x != y)

# Equal
print ("x == y: ", x == y)
